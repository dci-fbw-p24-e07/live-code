""" 
Your task is to check if given value is the instance of 
given class type using isinstance() function.
The first argument of this function should be value, 
for example city and the second argument should be class type, for example int.
Print at least one result for every primitive data 
type (int, float, bool, complex, and str).
"""

# Integer
print(isinstance(123, int))

# Float
print(isinstance(43.23, float))

# Complex
print(isinstance(4-1j, complex))

# String
print(isinstance("How are you?", str))

# Boolean
print(isinstance(True, bool))
